                    <h2>WHY YOUFUNDAFRICA</h2>
                    <p>There are quite a number of crowd funding and funding platforms designed for sponsoring or backing innovative ideas and important causes focused towards making positive and meaningful impact. These platforms have been thriving well enough in serving as solution providers to individuals and corporate idealists in need of funds to execute projects and launch innovative product lines.
                    Africa however, has not been able to tap into this democratic opportunity that could be explored and exploited for the purpose of fostering developmental projects and ideas in unreached and underserved communities.
                    Therefore, there is a need to create a revolutionary way of funding projects and ideas especially for the purpose of sustainable community development. This is why the creation of an Africa-focused, globally-funded crowd funding platform like youFUNDAfrica needs to be birthed to predominantly fund grassroots developmental ideas.<br><br>As much as we desire to accommodate all kinds of projects, yFA would only focus on subscribers who are keen on starting up community based socio-enterprise or industrialization. And each subscriber would be approved based on how the ideas and projects are:
                    Impactful | Sustainable
                    Scalable | Profitable | Feasible
                    Any project that does not have the community at the core of its project would not be approved. This is because we want the funds to be as evenly concentrated and distributed as possible. This may fortunately result to the high donor-to-recipient ratio.
                    </p>
