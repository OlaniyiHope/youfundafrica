                      <h2>DONORS</h2>
                    <p>This platform unlike most crowd funding platform will have an account page where donors could make fund deposits prior to the emergence of projects that best match their profile. It would be like an investment banking version of crowd funding whereby automation would be designed to help donors disburse their deposited funds based on their profiles. This automation would be a recommended option and not a compulsion. As many backers or donors that funded any projects would receive 1% increase of whatever they donated and addedto their yFA accounts. The donors would be allowed to review and give feedbacks as regards how transparent and accountable recipients are.  In addition to the criticality and usefulness of ideas, regular donors would be given considerations when they become recipients. This applies to African donors.</p>
