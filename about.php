                      <h2>WHAT WE DO</h2>
                    <p>Because we are result-driven and ourdedication towards qualitative projects, yFA will also be streamlined  to supporting causes that fall within these thematic fields of engagements: Technology | Education Health | Agriculture We believe that any community that  is developed in these areas has attained the level of sustainability and productivity will always be well assured. Any idealist or start-up who subscribes to yFA must be working in any of these thematic areas. More prudence would be given to any start-up or individual whose ideas or projects have a considerably greater depth in one of these areas. More attention would be given to those who find a way to intricately woven the interaction of two or more thematic areas together thereby offering both depth and coverage.</p>
                       <h2>RECIPENTS</h2>
                    <p>7.5% of the funds raised would be the fee for using yFA as a crowd funding platform. The fee will be higher if perks offered and brand elements are part of the deal for using yFA.2.5% of every fund raised by the recipients would be retained for the purpose of donating to any project or idea that the recipients find worthwhile. The recipients become the donor. This is meant to inculcate a sense of giving and crowd funding in Africa. We want every African that subscribe as recipients to become donors in the long run. This is the crux of yFA – for Africans to develop a camaraderie sense when it comes to crowd funding!</p>
               
                   
                    <h2>BRAND ELEMENTS</h2>
                    <p>yFA would offer to help recipients with the production of brand elements they plan to give as perks to donors. This would motivate them to want to give more towards causes on yFA. The expenses for this service would be deducted from the funds raised so recipients would be advised to add these costs to the target. The price of each brand element would have been pre-set.</p>
